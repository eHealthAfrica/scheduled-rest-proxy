from ndscheduler import job

from . import handle_job


class RESTForwarder(job.JobBase):

    @classmethod
    def meta_info(cls):
        return {
            'job_class_string': '%s.%s' % (cls.__module__, cls.__name__),
            'notes': 'This sends a HTTP request to a particular URL',
            'arguments': {},
            'example_arguments': ('a', 'n-arg')
        }

    def run(self, *args, **kwargs):
        config = (kwargs.get('json_config', {}))
        return handle_job(config)
